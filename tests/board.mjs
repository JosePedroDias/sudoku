import tap from 'tap';

import { Board } from '../board.mjs';

tap.test('getStateAscii', (t) => {
    const b = new Board();
    b.setState81('500420800403070010010060000020734006000205000100698040000080020060040905008057001');
    tap.equal(b.getStateAscii(),
`.-----.-----.-----.
|5 . .|4 2 .|8 . .|
|4 . 3|. 7 .|. 1 .|
|. 1 .|. 6 .|. . .|
:----- ----- -----:
|. 2 .|7 3 4|. . 6|
|. . .|2 . 5|. . .|
|1 . .|6 9 8|. 4 .|
:----- ----- -----:
|. . .|. 8 .|. 2 .|
|. 6 .|. 4 .|9 . 5|
|. . 8|. 5 7|. . 1|
'-----'-----'-----'`);
    t.end();
});
